namespace BlazorAppUserPortal
{
    /// <summary>
    /// "User" class is created that represents as the data model to work with. 
    /// This class has 7 properties(ID, First Name , Middle Name, Last Name,Gender, City and Occupation)
    /// </summary>
    public class User
    {
        public int Id { get; set; }

        public string FirstName { get; set; }

        public string MiddleName { get; set; }

        public string LastName { get; set; }

        public string Gender { get; set; }

        public string City { get; set; }

        public string Occupation { get; set; }

    }
}





